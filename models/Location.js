// File    : models/Location.js
// Purpose : Mongoose schema for physical locations (e.g., offices or branches)
// Used in : locationController, departmentController, attendanceController
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

/* ============================================================================
 * 1. Imports
 * ========================================================================== */
const mongoose = require('mongoose');

/* ============================================================================
 * 2. Schema Definition
 * ========================================================================== */
const locationSchema = new mongoose.Schema({
  // ðŸ”¹ Location name (e.g., "Head Office", "Branch A")
  name: {
    type: String,
    required: true,
    unique: true
  },

  // ðŸ”¹ Latitude (e.g., 24.7136)
  latitude: {
    type: Number,
    required: true
  },

  // ðŸ”¹ Longitude (e.g., 46.6753)
  longitude: {
    type: Number,
    required: true
  },

  // ðŸ”¹ Radius (in meters) used for attendance geofencing
  radius: {
    type: Number,
    required: true
  }

}, { timestamps: true }); // Adds createdAt and updatedAt automatically

/* ============================================================================
 * 3. Export Model
 * ========================================================================== */
module.exports = mongoose.model('Location', locationSchema);
